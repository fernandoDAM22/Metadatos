    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Im√°genes", "jpg", "jpeg", "png", "bmp", "gif"));
        if (fileChooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            labelRuta.setText(file.getAbsolutePath());
        }
    }


    private void btnExtraerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExtraerActionPerformed
        //comprobamos que el campo de la ruta no esta vacio
        if(labelRuta.getText().equals("")){
            return;
        }
        HashMap<String, String> metadataMap = Data.extractImageMetadata(labelRuta.getText());
        //comprobamos que la ruta es correcta
        if(metadataMap == null){
            JOptionPane.showMessageDialog(this,"La ruta no es valida","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Limpiar la tabla antes de agregar nuevos datos
        DefaultTableModel model = (DefaultTableModel) tablaDatos.getModel();

        model.setRowCount(0);

        // Agregar cada fila de datos a la tabla
        for (String key : metadataMap.keySet()) {
            model.addRow(new Object[] { key, metadataMap.get(key) });
        }
        //le ponemos los colores a la tabla
        tablaDatos.getColumnModel().getColumn(0).setCellRenderer(new MyCellRenderer());
        tablaDatos.getColumnModel().getColumn(1).setCellRenderer(new MyCellRenderer());



    }//GEN-LAST:event_btnExtraerActionPerformed


